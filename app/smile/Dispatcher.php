<?php
namespace App\Smile;

class Dispatcher
{
    public $name = null;
    
    public function __construct()
    {
        global $routes;
        return $this;
    }

    /**
     * Get the value of name
     */ 
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set the value of name
     *
     * @return  self
     */ 
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }
}